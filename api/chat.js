// 言語を判定する（日本語・インドネシア語・英語）
function detectLanguage(text) {
  if (/[\u3040-\u309F\u30A0-\u30FF\u4E00-\u9FFF]/.test(text)) return 'ja';
  if (/\b(yang|dan|untuk|dengan|dari|ini|itu|adalah|tidak|akan|bisa|sudah|belum|apa|bagaimana|pajak|perusahaan)\b/i.test(text)) return 'id';
  return 'en';
}

// ログ保存（失敗しても無視する＝ユーザーへの回答に影響しない）
async function saveLog(question, answer, language) {
  if (!supabase) return;
  try {
    await supabase.from('logs').insert({ question, answer, language });
    console.log('Log saved');
  } catch (e) {
    console.error('Log save failed:', e.message);
  }
}

module.exports = async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'POST only' });

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) return res.status(500).json({ error: 'APIキーが設定されていません。Vercelの環境変数を確認してください。' });

  const { messages } = req.body || {};
  if (!messages) return res.status(400).json({ error: 'messages required' });

  // ユーザーの最後の質問を取得（ログ用）
  const lastUserMessage = [...messages].reverse().find(m => m.role === 'user');
  const userQuestion = lastUserMessage ? String(lastUserMessage.content || '').slice(0, 2000) : '';

  const body = JSON.stringify({
    model: 'claude-opus-4-5',
    max_tokens: 2048,
    system: SYSTEM_PROMPT,
    messages: messages.slice(-10).map(m => ({
      role: m.role === 'assistant' ? 'assistant' : 'user',
      content: String(m.content || '').slice(0, 2000),
    })),
  });

  return new Promise((resolve) => {
    const options = {
      hostname: 'api.anthropic.com',
      path: '/v1/messages',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
        'Content-Length': Buffer.byteLength(body),
      },
    };

    const req2 = https.request(options, (resp) => {
      let data = '';
      resp.on('data', chunk => data += chunk);
      resp.on('end', async () => {
        try {
          const parsed = JSON.parse(data);
          if (resp.statusCode !== 200) {
            res.status(500).json({ error: `Anthropic Error ${resp.statusCode}: ${JSON.stringify(parsed)}` });
          } else {
            const text = parsed.content?.filter(b => b.type === 'text').map(b => b.text).join('\n') || '';
            res.status(200).json({ answer: text });

            // ログ保存（回答を返した後に実行。失敗しても影響なし）
            await saveLog(userQuestion, text, detectLanguage(userQuestion));
          }
        } catch (e) {
          res.status(500).json({ error: `Parse error: ${data}` });
        }
        resolve();
      });
    });

    req2.on('error', (e) => {
      res.status(500).json({ error: `Network error: ${e.message}` });
      resolve();
    });

    req2.write(body);
    req2.end();
  });
};
